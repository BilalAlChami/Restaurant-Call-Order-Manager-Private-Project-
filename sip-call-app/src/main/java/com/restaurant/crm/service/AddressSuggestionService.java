    public List<String> getAddressSuggestions(String query) {
        if (query == null || query.trim().length() < 2) { //
            return Collections.emptyList(); //
        }
        String lowerCaseQuery = query.toLowerCase().trim(); //

        return sampleStreetAddresses.stream()
                .filter(address -> address.toLowerCase().contains(lowerCaseQuery)) //
                .limit(15) //
                .collect(Collectors.toList()); //
    }

    /**
     * Finds the city for a given 5-digit PLZ.
     * Assumes the format "PLZ Stadt Straßenname" in sampleStreetAddresses.
     * @param plz The 5-digit postal code.
     * @return The city name, or null if not found.
     */
    public String getCityForPlz(String plz) {
        if (plz == null || !plz.matches("\\d{5}")) {
            return null;
        }
        for (String address : sampleStreetAddresses) { // Iterates over addresses like "64295 Darmstadt Am Blauen Stein"
            if (address.startsWith(plz + " ")) {
                // Example: "64295 Darmstadt Am Blauen Stein"
                // parts will be ["64295", "Darmstadt", "Am", "Blauen", "Stein"]
                String[] parts = address.split("\\s+", 3); // Split into PLZ, City, and rest (Street)
                if (parts.length > 1) {
                    // parts[0] is PLZ
                    // parts[1] is assumed to be the City. This works for single-word or hyphenated city names in your data.
                    // E.g., "Darmstadt", "Ober-Ramstadt"
                    return parts[1];
                }
            }
        }
        return null; // No city found for this PLZ
    }
